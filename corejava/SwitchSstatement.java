package corejava;

public class SwitchSstatement {
	
	public static void main(String[]args) {
		
		String name="anvika";
		
		switch(name) {
		case "abhijeet":
			
			System.out.println("My name is abhijeet");
			break;
			
		case "anvika":
			System.out.println("My name is anvika");
			break;
		default :
			System.out.println("This is default statement");
		}
		   System.out.println("this is the outside the statement");
		   
			
			
		}
	}
	
